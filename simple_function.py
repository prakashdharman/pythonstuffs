# Define a simple function that adds two numbers
def add_numbers(x, y):
    return x + y

# Define another function that subtracts two numbers
def subtract_numbers(x, y):
    return x - y

# Define a function that prints a greeting
def greet(name):
    print(f"Hello, {name}!")

# Call the functions with different arguments
sum_result = add_numbers(3, 5)
print("Sum:", sum_result)

difference_result = subtract_numbers(8, 3)
print("Difference:", difference_result)

greet("Alice")
